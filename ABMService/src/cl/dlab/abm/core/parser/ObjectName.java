package cl.dlab.abm.core.parser;

public class ObjectName
{
	private String name;
	public ObjectName(String name)
	{
		this.name = name;
	}
	/**
	 * @return the name
	 */
	public String getName()
	{
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name)
	{
		this.name = name;
	}
}
